int	ft_str_is_alpha(char *str)
{
	int	i;
	int	k;
	int	s;

	i = 0;
	k = 1;
	while (str[i] != '\0')
	{
		s = str[i];
		if ((s >= 'A' && s <= 'Z') || (s >= 'a' && s <= 'z'))
			k = 1;
		else
			return (0);
		i++;
	}
	return (k);
}
