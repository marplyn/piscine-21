int	ft_str_is_numeric(char *str)
{
	int	i;
	int	k;
	int	s;

	i = 0;
	k = 1;
	while (str[i] != '\0')
	{
		s = str[i];
		if (s >= '0' && s <= '9')
			k = 1;
		else
			return (0);
		i++;
	}
	return (k);
}
