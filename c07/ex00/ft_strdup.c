#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*str;
	char	*t;

	i = 0;
	while (src[i])
		i++;
	str = malloc(i + 1);
	t = str;
	while (*src)
		*t++ = *src++;
	*t = '\0';
	return (str);
}
